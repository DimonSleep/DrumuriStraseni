							<h3>Un reportaj Despre Proiectul Drumuri Bune din Satul Greblesti anul 2018</h3>
							<p>
                Deşi nu locuiesc decât la 35 de kilometri de Capitală, la ei asfaltul a ajuns abia ieri. Vorbim despre locuitorii din Grebleşti, raionul Străşeni, pentru care darea în exploatare a unui kilometru de drum a fost un prilej de adevărată sărbătoare.
                <br>
                Porțiunea a fost construită în cadrul programului guvernamental, dar şi cu suportul localnicilor şi a autorităţilor locale.
                <br>
                Odată cu asfaltarea drumului principal din localitate, bicicletele şi rolele au apărut în familiile cu copii ca ciupercile după ploaie: "Mai înainte nu îl aveam, acum câteva zile l-am cumpărat. - De ce l-ai cumpărat? - L-am cumpărat fiindcă ne-au făcut drumul acum".
                <br>
                Gheorghe Chircă este şofer de mai bine de 50 ani şi spune că în tot acest răstimp în satul său nu a văzut vreodată să se asfalteze drumul: "Eu am dat acum 9.500 de lei pentru reparaţie, aici a fost cutia, caroserie".
                <br>
                Şi pentru localnici drumul plin de noroi era o adevărată durere de cap: "Mai ales când ploua era straşnic. Noi maturii ne mai descurcăm, dar copiii mai greu. Acum o să purtăm şi noi pantofi".
                <br>
                600 de metri au fost făcuţi în cadrul programului "Drumuri Bune", iar alţi 500 de metri de Consiliul local împreună cu Primăria. O mână de ajutor financiar au oferit şi 24 de gospodării, contribuţiilor acestora variază între 500 şi trei mii de lei.
                <br>
                "Erau aici nişte gropi, de doamne fereşte. Şi găuri şi praf şi ce doreşti", a spus o localnică.
                <br.
                "Veneam în galoşi şi aici ne schimbam în papuci mai curaţi. Aşa trăim. Acum şi desculţă putem să mergem", a afirmat o altă localnică.
                <br>
                "O să umblăm deja în pantofi albi", a spus o femeie.
                <br>
                Darea în exploatare a porţiunii de drum s-a făcut sub privirea atentă a premierului Pavel Filip.
                <br>
                "Doar un drum este din proiectul "Drumuri Bune" pentru Moldova, celelalte este finanţat de către Consiliul Raional, dar o parte din bani este acordat şi de către Primărie. Apropo suma nu este mică din partea Primăriei de 800 de mii de lei, ceea ce înseamnă că sunt gospodari în localitatea aceasta şi nu sunt săraci", a declarat premierul Pavel Filip.
                <br>
                Asfaltarea drumurilor din satul Grebleşti nu se opreşte aici. La anul, localitatea va beneficia şi de programul "Drumuri Bune 2", lansat de liderul PDM, Vlad Plahotniuc.
                <br>
                <a href="https://www.publika.md/bucurie-pentru-locuitorii-din-greblesti-raionul-straseni-primul-kilometru-de-drum-asfaltat-din-sat-dat-in--exploatare_3021645.html#ixzz7H8YKQg4m" target="_blank"><font style="color:#008BC9">Vezi tot reportajul</font></a>
              </p>


              <h3>Programul "Drumuri Bune 2 pentru Moldova", subiect discutat în cadrul vizitei de lucru a Cabinetului de miniștri, în raionul Strășeni anul 2018 </h3>
              <p>
                Inițierea procesului de colectare a propunerilor privind sectoarele de drum care urmează a fi incluse în Programul Guvernamental „Drumuri Bune 2 pentru Moldova”, gazificarea, aprovizionarea  cu apă a localităților, asigurarea cu energie electrică, atragerea investițiilor în localitățile rurale - acestea au fost unele dintre subiectele discutate astăzi cu locuitorii și autoritățile publice locale din raionul Strășeni, în cadrul vizitei de lucru a membrilor Guvernului în teritoriu.
                 <br>
                În cadrul discuțiilor, ministrul Economiei și Infrastructurii, Chiril Gaburici, și secretarii de stat au răspuns la întrebările ce țin de infrastructura drumurilor, transport, dezvoltarea businessului mic și mijlociu, precum și crearea locurilor noi de muncă.
                 <br>
                Reprezentanții autorităților locale au semnalat necesitatea stringentă de a reproiecta construcția drumului de la intrarea în Strășeni, motivând că din cauza apelor subterane și a nămolului din răul Bâc, se deteriorează suprafața drumului. În acest sens, ministrul Economiei și Infrastructurii a menționat că ”Ultimele soluții de reproiectare au fost aprobate și puse în aplicare din luna septembrie, curent. Deja se lucrează lângă lacul Ghidighici, aceeași soluție va fi folosită și la drumul din Strășeni. Planificăm finalizarea acestuia în primele luni ale anului viitor”, a precizat ministrul.
                 <br>
                Totodată, cetățenii și-au manifestat mulțumirea față de lansarea procesului de colectarea a datelor pentru elaborarea proiectului Programului „Drumuri Bune 2 pentru Moldova”. Motivând că, acest proiect este unul de mare utilitate pentru cetățeni, iar reabilitarea drumurilor va impulsiona economia nu doar în raionul Strășeni, dar și în regiune.
                 <br>
                În cadrul vizitei în teritoriu, reprezentanții Ministerului Economiei și Infrastructurii au continuat dialogul cu locuitorii din satele Chirianca și Tătărești. Printre subiectele enunțate de localnici sunt asigurarea transportului public de călători, aprovizionarea  cu apă a localităților, precum și gazificarea acestora. Echipa Ministerului Economiei și Infrastructurii a vorbit cetățenilor despre programele de susținere din partea autorităților statului pentru lansarea afacerilor și dezvoltarea întreprinderile mici și mijlocii, și a încurajat locuitorii din raionul Strășeni să investească acasă.
                <br>
                <a href="https://www.publika.md/programul-drumuri-bune-2-pentru-moldova-subiect-discutat-in-cadrul-vizitei-de-lucru-a-cabinetului-de-ministri-in-raionul-straseni_3022370.html#ixzz7H8ZKaJZX" target="_blank"><font style="color:#008BC9">Vezi tot reportajul</font></a>
              </p>


                <h3>Start construcției drumurilor în 3 sate , 2019</h3>
                <p>
                Locuitorii din satele Țigănești, Onești și Codreanca, din raionul Strășeni, se vor putea deplasa mai ușor pe singurul drum care leagă aceste sate. Marți, 19 februarie, au început lucrările de reabilitare a drumului pe o porțiune de 4,4 km.
                <br>
                În prima etapă, drumul va fi acoperit cu un strat de piatră, apoi, în cadrul programului guvernamental Drumuri Bune 2, va fi reabilitat capital, transmite Știri.md cu referire la telegraph.md.
                <br>
                ”Va fi aplicat un strat de pietriș de 15 cm. În prima etapă va fi reabilitat drumul pe o lungime totală de 4,4 km”, a confirmat Vladimir Croitor, directorul întreprinderii ”Drumuri Strășeni”.
                <br>
                Administrația locală spune că reparația drumului era foarte necesară, deoarece acesta reprezintă singura lor cale de acces spre traseul național care face legătura cu centru raional, dar și cu capitala. ”Acest drum pentru locuitorii satului este de o importanță majoră. Ne bucurăm că, în sfârșit, s-a reușit să obținem acest proiect și să avem un drum bun, de calitate”, afirmă Valentina Țaca, primarul satului Țigănești.
                <br>
                Și locuitorii satelor sunt mulțumiți că Guvernul a intervenit pentru reabilitarea drumului. ”Desigur că e o bucurie mare și dacă va fi făcut totul cum s-a promis, poate va atrage și tineretul înapoi la țară”, a spus un locuitor. ”E tare greu, în primul rând, se strică toată tehnica. În al doilea rând, în alte sate se face, dar la noi în sat de ce să nu se facă?”, a spus alt locuitor.
                <br>
                Cheltuielile pentru pietruirea drumului, în mărime de 1,5 milioane de lei, sunt acoperite de Consiliului raional Strășeni. De asemenea, la inițiativa premierului Pavel Filip, drumul a fost inclus în programul ”Drumuri Bune 2”, iar pentru reabilitarea lui au mai fost alocate încă 8 milioane de lei.
			  </p>
			  

